#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*d;
	size_t			i;

	i = 0;
	d = (unsigned char*)s;
	while (i < n)
	{
		d[i] = 0;
		i++;
	}
}
